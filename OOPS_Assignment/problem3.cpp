#include<iostream>
using namespace std;
int main() {
	int a,b=1,c;
	cout<<" Enter Number To Find Its Factorial:  ";
	cin>>c;
	for (int a=1;a<=c;a++) {
		b=b*a;
	}
	cout<<"Factorial of Given Number is   "<<b<<endl;
	return 0;
}
